/**
 * Given an array of domains, return the object with the appearances of the DNS.
 *
 * @param {Array} domains
 * @return {Object}
 *
 * @example
 * domains = [
 *  'code.yandex.ru',
 *  'music.yandex.ru',
 *  'yandex.ru'
 * ]
 *
 * The result should be the following:
 * {
 *   '.ru': 3,
 *   '.ru.yandex': 3,
 *   '.ru.yandex.code': 1,
 *   '.ru.yandex.music': 1,
 * }
 *
 */
function getDNSStats(domains) {

  let domainArr = domains.map(x => x.split('.').reverse())
  let levelDNSArr = []
  domains.forEach(e => {levelDNSArr.push(e.split('.').length)});
  let levelDNS = Math.max(...levelDNSArr)
  
  let res = {}

  for (i = 0; i < levelDNS; i++) {
    domainArr.forEach( (x, index) => {
      if((x.length > i)) {
        currentDNS = x.slice(0, i + 1).join('.')
        if(res.hasOwnProperty(`.${currentDNS}`) ){
              res[`.${currentDNS}`]++
            } else {
              res[`.${currentDNS}`] = 1
            }

      }
    })
  }

  return res


}

module.exports = {
  getDNSStats
};
